package week1.day1;

public class LearnFor {
	
// Looping statement
// Same task is performed multiple number of times	
// number of iterations is well known ahead	
	public static void main(String[] args) {
		
		System.out.println("1");
		System.out.println("2");
		System.out.println("3");
		System.out.println("4");
		System.out.println("5");
		
		System.out.println("**********************");
		// intialization ; condition ; iteration/increment
		// intialization : start value
		// condition : end value
		// increment : take me to the next value
		// i++ --> post increment operator ; (++i)
		// i++  --> i = i+1
		
		for (int i = 3; i <= 10 ; i++) {
			System.out.println(i);
		}
		
		// print all the even numbers between 1 and 50
		// main task : find the even numbers  - IF()
		// multiple times : for
		
		
		
		
		

		
		
		
      
	}

}
