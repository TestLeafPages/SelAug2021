package week1.day1;

// Subject : MobilePhone
public class LearnDataTypesAndvariables {
	public static void main(String[] args) {
		// syntax: 
		// dataType variableName = value/literal
		// Non Primitive data type
		// can perform operations on these data
		String brandName = "Samsung";  // class
		String model = "A6+Galaxy-&%$3)";
		
		// Primitive data type
		// can store the values
		char color = 'B';
		int price = 40000;
		byte memory =    120;  // -128 to 127
		float screenSize = 14.5f;
		boolean isTouchScreen = true;
		long phoneNumber = 9943564859L;
		
		// + --> concatenation operator 
		System.out.println("Brand Name : -->" +  brandName);
		System.out.println(" Price of " + brandName + " is " + price );
		
		
		
		
		
		
		

		
		
	}

}
